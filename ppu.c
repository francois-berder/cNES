/***************************
 * PPU_CTRL FUNCTIONS      * 
 * *************************/
uint16_t ppu_nametable_addr()
{
	switch(PPU_CTRL & 0x03) {
	case 0:
		return 0x2000;
	case 1:
		return 0x2400;
	case 2:
		return 0x2800;
	case 3:
		return 0x2C00;
	default:
		return 0x2000;
	}
}

uint8_t ppu_vram_addr_inc()
{
	if ((PPU_CTRL & 0x04) == 0) {
		return 1;
	} else {
		return 32;
	}
}

uint16_t ppu_sprite_pattern_table_addr()
{
	/* Sprite Table Bit (3) */
	if ((PPU_CTRL & 0x08) == 0) {
		return 0x0000;
	} else {
		return 0x1000;
	}
}

uint16_t ppu_bg_pattern_table_addr()
{
	/* Pattern Table Bit (4) */
	if ((PPU_CTRL & 0x10) == 0) {
		return 0x0000;
	} else {
		return 0x1000;
	}
}

uint8_t ppu_sprite_height()
{
	/* Sprite Size (Height) Bit (5)*/
	if ((PPU_CTRL & 0x20) == 0) {
		return 8;
	} else {
		return 16;
	}
}

